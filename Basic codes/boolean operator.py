# boolean operator are 2 types: True and False

# comparison operator are 6 types
# equality ==
print(10 == 10)
print(9 == 9.0)
print(9 == "9")
print(9 == 10)
# not equal !=
print(10 != 10)
print(9 != 10)
# less than <
print(9 < 10)
print(11 < 10)
# greater than >
print(9 > 10)
print(11 > 10)
# less than or equal <=
print(9 <= 10)
print(10 <= 10)
# greater than or equal >=
print(9 >= 10)
print(11 >= 10)

# binary boolean operator
# and both should be true
print(2 == 2 and 5 == 5)
print(2 == 3 and 5 == 5)
print(True and True)
print(True and False)
# or if any part true, it wil be true
print(2 == 2 or 5 == 5)
print(2 == 3 or 5 == 5)
print(True or True)
print(True or False)
print(False or False)
# not ja true ta false kore dibe
print(not 10 == 10)
print(not 9 == 10)
print(not True)

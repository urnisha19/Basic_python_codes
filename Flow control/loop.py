# while loop
x = 0
while x <= 5:
    print(x)
    x = x + 1

# for loop
for i in range(1, 4):
    print("hello")
print("end of 1st for loop")

for j in range(1, 4, 2):
    print(j)
print("end of 2nd for loop")

# for loop negative end point, this loop will decrease step value
for k in range(5, -5, -2):
    print(k)
